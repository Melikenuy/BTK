tutttuğumsayi=3
while True:
        sayi=int(input("bir sayı girirniz: "))
        if sayi>tutttuğumsayi:
            print("girdiğiniz sayıyı küçültün")

        elif sayi<tutttuğumsayi:
            print("girdiğiniz sayıyı büyültün")

        else :
            print("tebrikler sayıyı tutturdunuz")
            break